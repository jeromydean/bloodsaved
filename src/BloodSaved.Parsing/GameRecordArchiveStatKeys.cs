    private static readonly IReadOnlyDictionary<string, IReadOnlyList<string>> DemonArchiveAliasKeys = new Dictionary<string, IReadOnlyList<string>>
    {
      ["N1009"] = ["N1009_Enemy"],
      ["N3084"] = ["N3001_Armor"],
    };

    internal static IReadOnlyList<string> DemonArchiveKeys => DemonKillKeys
      .Concat(DemonArchiveAliasKeys.Values.SelectMany(aliasKeys => aliasKeys))
      .Distinct(StringComparer.Ordinal)
      .ToArray();

    internal static IReadOnlyList<string> GetDemonArchiveKeysForDemons(IEnumerable<string> demonKeys)
    {
      return demonKeys
        .SelectMany(demonKey => DemonArchiveAliasKeys.TryGetValue(demonKey, out IReadOnlyList<string>? aliasKeys)
          ? aliasKeys.Prepend(demonKey)
          : [demonKey])
        .Distinct(StringComparer.Ordinal)
        .ToArray();
    }

    private static readonly IReadOnlyDictionary<string, string> ShardItemKeysByName = Enum.GetValues<ItemId>()
      .Where(itemId => itemId.GetCategory() >= ItemCategory.ConjureShards)
      .GroupBy(itemId => itemId.GetName(), StringComparer.Ordinal)
      .ToDictionary(
        group => group.Key,
        group => group.First().GetIdString(),
        StringComparer.Ordinal);

    internal static IReadOnlyList<string> GetShardItemKeysForDemons(IEnumerable<string> demonKeys)
    {
      return demonKeys
        .Select(demonKey => DemonShardNames.TryGetValue(demonKey, out string? shardName) ? shardName : null)
        .OfType<string>()
        .Select(shardName => ShardItemKeysByName.TryGetValue(shardName, out string? itemKey) ? itemKey : null)
        .OfType<string>()
        .Distinct(StringComparer.Ordinal)
        .ToArray();
    }

